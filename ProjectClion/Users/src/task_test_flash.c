#define SIZE_BUF            (0x400UL)

// Test constants
#define TEST_FLASH_NUMBER_OF_ITERATIONS_TEST_1          (3U)

//**************************************************************************************************
// Definitions of static global (private) variables
//**************************************************************************************************

uint8_t dataRead[SIZE_BUF];
uint8_t dataWrite[SIZE_BUF];


